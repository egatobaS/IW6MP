float AngleNormalize360(float angle) {
	if ((angle >= 360.0f) || (angle < 0.0f)) {
		angle -= floorf(angle * (1.0f / 360.0f)) * 360.0f;
	}
	return angle;
}

Vector3 VectorNormalize(Vector3 vec)
{
	float length = 1.0f / sqrt(DotProduct(vec, vec));
	vec.x *= length;
	vec.y *= length;
	vec.z *= length;
	return vec;
}

